    public String getPickupReview() { return pickupReview; }
    public void setPickupReview(String value) { this.pickupReview = value; }

    public String getWaitTime() { return waitTime; }
    public void setWaitTime(String value) { this.waitTime = value; }

    public String getAveWaitMins() { return aveWaitMins; }
    public void setAveWaitMins(String value) { this.aveWaitMins = value; }

    public String getText() { return text; }
