package com.carTrawlerDemo;

public class Vendor {
    private String code;
    private Context codeContext;
    private String companyShortName;
    private String division;

    public String getCode() { return code; }
    public void setCode(String value) { this.code = value; }

    public Context getCodeContext() { return codeContext; }
    public void setCodeContext(Context value) { this.codeContext = value; }

    public String getCompanyShortName() { return companyShortName; }
    public void setCompanyShortName(String value) { this.companyShortName = value; }

    public String getDivision() { return division; }
    public void setDivision(String value) { this.division = value; }
}
