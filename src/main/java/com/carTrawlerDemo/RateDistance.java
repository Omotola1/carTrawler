package com.carTrawlerDemo;

public class RateDistance {
    private String distUnitName;
    private String quantity;
    private String unlimited;
    private String vehiclePeriodUnitName;

    public String getDistUnitName() { return distUnitName; }
    public void setDistUnitName(String value) { this.distUnitName = value; }

    public String getQuantity() { return quantity; }
    public void setQuantity(String value) { this.quantity = value; }

    public String getUnlimited() { return unlimited; }
    public void setUnlimited(String value) { this.unlimited = value; }

    public String getVehiclePeriodUnitName() { return vehiclePeriodUnitName; }
    public void setVehiclePeriodUnitName(String value) { this.vehiclePeriodUnitName = value; }
}
