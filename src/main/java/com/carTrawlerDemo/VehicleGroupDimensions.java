package com.carTrawlerDemo;

public class VehicleGroupDimensions {
    private String vehicleLength;
    private String vehicleWidth;
    private String vehicleHeight;
    private String vehicleLoad;

    public String getVehicleLength() { return vehicleLength; }
    public void setVehicleLength(String value) { this.vehicleLength = value; }

    public String getVehicleWidth() { return vehicleWidth; }
    public void setVehicleWidth(String value) { this.vehicleWidth = value; }

    public String getVehicleHeight() { return vehicleHeight; }
    public void setVehicleHeight(String value) { this.vehicleHeight = value; }

    public String getVehicleLoad() { return vehicleLoad; }
    public void setVehicleLoad(String value) { this.vehicleLoad = value; }
}
