package com.carTrawlerDemo;

public class Config {
    private String orderBy;
    private String relevance;
    private String preferred;
    private String insurance;
    private String duration;
    private String limited;
    private String uiFilter;
    private String ccInfo;
    private String physicalInetAddress;
    private String consumerAddress;
    private String hotelDelivery;
    private String bestPrice;

    public String getOrderBy() { return orderBy; }
    public void setOrderBy(String value) { this.orderBy = value; }

    public String getRelevance() { return relevance; }
    public void setRelevance(String value) { this.relevance = value; }

    public String getPreferred() { return preferred; }
    public void setPreferred(String value) { this.preferred = value; }

    public String getInsurance() { return insurance; }
    public void setInsurance(String value) { this.insurance = value; }

    public String getDuration() { return duration; }
    public void setDuration(String value) { this.duration = value; }

    public String getLimited() { return limited; }
    public void setLimited(String value) { this.limited = value; }

    public String getUIFilter() { return uiFilter; }
    public void setUIFilter(String value) { this.uiFilter = value; }

    public String getCcInfo() { return ccInfo; }
    public void setCcInfo(String value) { this.ccInfo = value; }

    public String getPhysicalInetAddress() { return physicalInetAddress; }
    public void setPhysicalInetAddress(String value) { this.physicalInetAddress = value; }

    public String getConsumerAddress() { return consumerAddress; }
    public void setConsumerAddress(String value) { this.consumerAddress = value; }

    public String getHotelDelivery() { return hotelDelivery; }
    public void setHotelDelivery(String value) { this.hotelDelivery = value; }

    public String getBestPrice() { return bestPrice; }
    public void setBestPrice(String value) { this.bestPrice = value; }
}
